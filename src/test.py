"""
A very simple math module.
"""

def add(a, b):
    """Adds two numbers together.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The sum of a and b.
    """
    return a + b

def subtract(a, b):
    """Subtracts the second number from the first.

    Args:
        a (int): The first number.
        b (int): The second number.

    Returns:
        int: The difference between a and b.
    """
    return a - b